    private void jButtonCadastrarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarCategoriaActionPerformed
        // TODO add your handling code here:
        if(!jTextFieldCategoria.getText().isEmpty()){
        CategoriasController categoriasController = new CategoriasController();
        categorias.setNome(jTextFieldCategoria.getText());
        categorias.setSegmento(jComboBoxSegmento.getSelectedItem().toString());
        System.out.println(jComboBoxSegmento.getSelectedItem().toString());
        categoriasController.salvarCategoria(categorias);
        tableModelCategorias.setCategorias(categoriasController.consultarCategorias());
        tableModelCategorias.fireTableDataChanged();
        } else {
            JOptionPane.showMessageDialog(jPanel1, "Preencha todos os campos para realizar o cadastro.");
        }
        
    }//GEN-LAST:event_jButtonCadastrarCategoriaActionPerformed


